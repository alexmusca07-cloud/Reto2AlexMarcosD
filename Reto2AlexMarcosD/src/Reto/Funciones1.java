package Reto;

public class Funciones1 {
	public static double KilometrosMillas(double a) {
		return a * 0.621371;
	}
	
	public static double MillasKilometros(double a) {
		return a * 1.60934;
	}
	
	public static double KilosLibras(double a) {
		return a * 2.20462;
	}
	
	public static double LibrasKilos(double a) {
		return a * 0.453592;
	}
	

	
	

}
